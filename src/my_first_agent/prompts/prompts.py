"""Agent 各阶段提示词模板。"""

PLAN_PROMPT = """你是一个专业的研究规划专家。你的任务是将用户的问题拆解为可执行的研究计划。

用户的问题是: {question}

请创建一个包含以下内容的研究计划：
1. **核心问题** — 用一句话概括要回答的核心问题
2. **子问题** — 将核心问题拆解为 2-4 个具体的子问题，每个子问题应该可以通过搜索来回答
3. **搜索关键词** — 为每个子问题提供 1-2 组搜索关键词

以 JSON 格式输出：
{{"core_question": "...", "sub_questions": [{{"question": "...", "search_keywords": ["...", "..."]}}]}}

只输出 JSON。"""

ANALYZE_PROMPT = """你是一个信息分析专家。你将看到一组网页搜索结果（标题、摘要和链接），
以及它们对应的研究子问题。

研究问题: {question}
子问题: {sub_question}

搜索结果:
{search_results}

请从这些结果中提取与子问题最相关的信息。注意：
1. 只基于搜索结果中的内容，不要编造信息
2. 标注每条信息的来源索引
3. 指出信息中是否存在矛盾或不确定性
4. 如果搜索结果不足以回答子问题，明确指出信息缺口

以 JSON 格式输出分析结果：
{{"findings": [{{"claim": "...", "source_index": 0, "confidence": "high|medium|low"}}], "info_gaps": [...], "summary": "..."}}"""

DRAFT_PROMPT = """你是一个研究报告撰写专家。你将基于下面收集到的研究信息，
撰写一份结构清晰、内容扎实的研究报告。

原始问题: {question}
研究计划: {plan}

收集到的信息:
{findings}

撰写要求：
1. 使用 Markdown 格式
2. 包含：概述、核心发现、详细分析、结论
3. 在每个关键点标注信息来源（标注格式: [来源 1]，[来源 2]）
4. 语言客观中立，区分确定的事实和推测
5. 如果信息不完整，明确指出现有信息的局限

直接输出报告内容。"""

REFLECT_PROMPT = """你是一个严格的评审专家。请对下面这份研究报告进行批判性评审。

原始问题: {question}
研究计划: {plan}

研究报告:
{draft}

从以下维度进行评审（每个维度 1-10 分）：
1. **完整性** — 报告是否回答了核心问题？覆盖了所有子问题？
2. **准确性** — 信息是否有依据？是否有可疑的断言？
3. **结构性** — 逻辑是否清晰？结构是否合理？
4. **客观性** — 是否区分了事实和推测？

然后判断：
- 如果任何维度低于 7 分：需要修改，请给出具体的修改建议
- 如果所有维度都 >= 7 分：报告质量合格

最后决定下一步：请给出 JSON 格式的评审结论。
{{"scores": {{"completeness": 0, "accuracy": 0, "structure": 0, "objectivity": 0}}, "needs_revision": true/false, "needs_more_research": true/false, "comments": "...", "revision_suggestions": ["...", "..."], "research_gaps": ["...", "..."]}}"""

REVISE_PROMPT = """你是一个认真负责的研究报告修订专家。根据评审意见，修改这份报告。

原始问题: {question}

报告初稿:
{draft}

评审意见:
{reflection}

请输出改进后的完整报告（Markdown 格式）。确保所有评审中指出的问题都得到了解决。"""
